export function getFiscalInfo(weekStartDate) {
  const key = weekStartDate instanceof Date
    ? weekStartDate.toISOString().split('T')[0]
    : weekStartDate;
  return FISCAL_CALENDAR[key] || null;
}

export function getFiscalLabel(weekStartDate) {
  const info = getFiscalInfo(weekStartDate);
  if (!info) return null;
  return `${info.month} Week ${info.weekInMonth} · Fiscal Week ${info.fiscalWeek} · ${info.quarter}`;
}
